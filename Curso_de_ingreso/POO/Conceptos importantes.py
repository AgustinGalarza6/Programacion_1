# CONCEPTOS BASICOS:


# Diferencia entre ellas:
# La clase es lo abstracto
# El instancia es el concreto de lo "abstracto" (abstracto se refiere a definicion de algo.). Es un objeto creado a partir de una clase.
# Un objeto es una instancia de algo o de una clase.


# Una clase es una deficion de como se va a instanciar de algo. (que variables va a tener, que metodos va a tener, etc)
# Y una clase es una clasificacion especifica.

# Herencia, hereda comportamientos y miebros de una clase

# Las funciones se encuentran siempre fuera de la clase.
#Función Pura

#Siempre devuelve el mismo resultado si se pasan los mismos argumentos
#Nunca depende de ningún estado/dato/cambio durante la ejecución de un programa
#Siempre devuelve algo

# Función Impura

#Cambiar el estado interno de cualquier argumento que se haya pasado
#Puede tener efecto sin devolver nada
#Escribir casos de prueba será un poco complicado ya que puede haber efectos secundarios


# Los metodos siempre se encuentran dentro de una clase.



# Una interfaz define un contrato. Cualquier clase que implemente ese contrato debe proporcionar una implementación de los miembros definidos en la interfaz.
# (un miembro puede ser una variable, un metodo, etc)


# Diferencia entre interfaz y clase abstracta: 
# La interfaz define comportamientos sin definir. (metodos)
# Por ejemplo, este perro ladra, nose como ladra, pero ladra.

# Y la clase abstracta define variables y metodos.
# Por ejemplo, este perro ladra 3 veces. (Se define que el perro ladra 3 veces.)